# ══════════════════════════════════════════════════════════════════════════════
# 1. GEOMETRY — Asymmetric Tip-Plate Capacitor
# ══════════════════════════════════════════════════════════════════════════════
# All geometry parameters are engineering choices, clearly labeled.
# They are NOT fundamental constants.

D_GAP   = 1.0e-3   # Gap distance [m] (1 mm — engineering choice)
R_TIP   = 1.0e-6   # Tip radius [m] (1 µm — engineering choice)
A_PLATE = 25.0e-4  # Electrode area [m²] (25 cm² — engineering choice)

# Aspect ratio (determines field enhancement)
ASPECT = D_GAP / R_TIP  # = 1000:1

# ══════════════════════════════════════════════════════════════════════════════
# 2. E-FIELD PROFILE — Hyperboloid-on-plane
# ══════════════════════════════════════════════════════════════════════════════

def E_field_profile(z, V_rms, d_gap, r_tip):
    """
    Electric field strength along the gap axis.
    
    Uses the classical hyperboloid-over-plane model:
        E(z) = V / (z × ln(2d/r))
    
    where z is the distance from the tip (z=0 at tip, z=d at plate).
    
    Args:
        z: Position along gap axis [m], measured from tip
        V_rms: Applied RMS voltage [V]
        d_gap: Gap distance [m]
        r_tip: Tip radius [m]
    
    Returns:
        E: Electric field magnitude [V/m]
    """
    log_factor = np.log(2.0 * d_gap / r_tip)
    # Clamp z to avoid singularity at tip (minimum z = r_tip)
    z_safe = np.maximum(z, r_tip)
    return V_rms / (z_safe * log_factor)

# ══════════════════════════════════════════════════════════════════════════════
# 3. AXIOM 4 — Non-linear Permittivity from Saturation
# ══════════════════════════════════════════════════════════════════════════════

def epsilon_eff(E_local, dx):
    """
    Compute the local effective permittivity using Axiom 4.
    
    The voltage across a cell of width dx is V_local = E × dx.
    The saturation factor S = √(1 − (V_local / V_yield)²).
    The effective permittivity is ε_eff = ε₀ × S.
    
    When V_local → V_yield: ε → 0 (capacitor saturates, open circuit).
    When V_local << V_yield: ε → ε₀ (linear dielectric).
    
    Uses V_YIELD = √α × V_SNAP = 43,652 V from the engine.
    """
    V_local = np.abs(E_local) * dx
    S = universal_saturation(V_local, V_YIELD)
    return float(EPSILON_0) * S

# ══════════════════════════════════════════════════════════════════════════════
# 4. ENERGY DENSITY — Asymmetric due to spatially varying ε
# ══════════════════════════════════════════════════════════════════════════════

def energy_density_profile(z, E, dx):
    """
    Compute the EM energy density u(z) = ½ ε_eff(z) |E(z)|².
    
    In standard linear EM: ε_eff = ε₀ everywhere → u is symmetric.
    In Axiom 4: ε_eff varies with E → u is asymmetric → net force.
    """
    eps = epsilon_eff(E, dx)
    return 0.5 * eps * E**2

# ══════════════════════════════════════════════════════════════════════════════
# 5. PONDEROMOTIVE FORCE — F = -du/dz
# ══════════════════════════════════════════════════════════════════════════════

def ponderomotive_force(z, u):
    """
    Compute the force density F = -du/dz [N/m³].
    
    Uses central differences on the energy density profile.
    """
    dz = z[1] - z[0]
    F = np.zeros_like(u)
    F[1:-1] = -(u[2:] - u[:-2]) / (2.0 * dz)
    # Forward/backward at edges
    F[0] = -(u[1] - u[0]) / dz
    F[-1] = -(u[-1] - u[-2]) / dz
    return F

# ══════════════════════════════════════════════════════════════════════════════
# MAIN SIMULATION
# ══════════════════════════════════════════════════════════════════════════════

def run_simulation():
    # ── PREREQUISITE GATE: identify operating regime ──
    regime = identify_regime("em_voltage", V_local=30e3)
    print()

    print("=" * 78)
    print("  PONDER-01: FIRST-PRINCIPLES THRUST FROM AXIOM 4 SATURATION")
    print("  All operators from ave.core — zero magic numbers")
    print("=" * 78)
    
    # ── Sweep over voltages ────────────────────────────────────────────────
    V_RMS_range = np.array([1e3, 5e3, 10e3, 20e3, 30e3, 40e3])
    
    # ── Spatial grid along gap axis ────────────────────────────────────────
    N_z = 10000
    z = np.linspace(R_TIP, D_GAP, N_z)  # From tip to plate
    dz = z[1] - z[0]
    
    print(f"\n  Geometry:")
    print(f"    Gap distance:    {D_GAP*1e3:.1f} mm")
    print(f"    Tip radius:      {R_TIP*1e6:.1f} µm")
    print(f"    Aspect ratio:    {ASPECT:.0f}:1")
    print(f"    Plate area:      {A_PLATE*1e4:.0f} cm²")
    print(f"    Grid points:     {N_z}")
    print(f"    Cell size (dz):  {dz*1e6:.2f} µm")
    print(f"\n  AVE Constants (from engine):")
    print(f"    V_YIELD = √α × V_SNAP = {V_YIELD/1e3:.2f} kV")
    print(f"    V_SNAP  = m_e c²/e     = {V_SNAP/1e3:.1f} kV")
    print(f"    ε₀      = {float(EPSILON_0):.6e} F/m")
    print(f"    Z₀      = {float(Z_0):.2f} Ω")
    
    # ── Results table ──────────────────────────────────────────────────────
    print(f"\n  {'V_rms (kV)':>12} {'E_tip (MV/m)':>14} {'ε_tip/ε₀':>10} {'ΔF_novel (nN)':>14} "
          f"{'F_class (µN)':>12}")
    print("  " + "-" * 66)
    
    results = []
    
    for V_rms in V_RMS_range:
        # E-field profile
        E = E_field_profile(z, V_rms, D_GAP, R_TIP)
        
        # Axiom 4 nonlinear energy density
        u_nonlinear = energy_density_profile(z, E, dz)
        
        # Linear comparison (ε = ε₀ everywhere)
        u_linear = 0.5 * float(EPSILON_0) * E**2
        
        # THE KEY INSIGHT:
        # In standard EM with a SEALED capacitor, the classical ponderomotive
        # force F = -∇(½ε₀|E|²) is enormous but is exactly balanced by the
        # electrode reaction forces (Newton's 3rd Law). No net thrust.
        #
        # In Axiom 4: ε varies spatially because of saturation.
        # The differential energy density Δu = u_nonlinear - u_linear
        # is NOT balanced by electrode forces because it arises from the
        # vacuum lattice itself, not from the metal.
        # The NOVEL thrust is F_novel = -d(Δu)/dz.
        
        delta_u = u_nonlinear - u_linear  # Negative where ε < ε₀ (saturation)
        
        # Force from the differential (the novel AVE prediction)
        F_novel = ponderomotive_force(z, delta_u)
        
        # Classical (for comparison — this is balanced in a sealed device)
        F_classical = ponderomotive_force(z, u_linear)
        
        # Integrate the NOVEL force over the gap volume (1D × area)
        F_net = np.trapezoid(F_novel, z) * A_PLATE   # [N]
        F_cls = np.trapezoid(F_classical, z) * A_PLATE  # [N]
        
        # Tip characterization
        E_tip = E[0]
        eps_tip = epsilon_eff(E[:1], dz)[0] / float(EPSILON_0)
        
        print(f"  {V_rms/1e3:12.1f} {E_tip/1e6:14.2f} {eps_tip:10.6f} "
              f"{F_net*1e9:12.4f} {F_cls*1e6:10.2f}")
        
        results.append({
            'V_rms': V_rms, 'E_tip': E_tip, 'eps_tip': eps_tip,
            'F_net': F_net, 'F_cls': F_cls,
            'z': z, 'E': E, 'u_nl': u_nonlinear, 'u_lin': u_linear,
            'delta_u': delta_u, 'F_novel': F_novel, 'F_classical': F_classical,
        })
    
    # ── Power and efficiency at the reference point ────────────────────────
    # Use 30 kV @ 100 MHz as the reference operating point
    V_ref = 30e3
    F_ref = 100e6
    
    # Parallel-plate capacitance (lower bound)
    C_gap = float(EPSILON_0) * A_PLATE / D_GAP
    
    # Reactive power (no loss): P_reactive = ½ ω C V²
    omega = 2 * np.pi * F_ref
    P_reactive = 0.5 * omega * C_gap * V_ref**2
    
    # Dissipated power depends on Q. Use Q from the HOPF-01 model.
    # For a tuned resonator, P_dissipated = P_reactive / Q
    Q_estimated = 100  # Engineering estimate (LABELED)
    P_dissipated = P_reactive / Q_estimated
    
    # Get thrust at 30 kV
    idx_30 = np.argmin(np.abs(V_RMS_range - 30e3))
    F_at_30kV = results[idx_30]['F_net']
    
    efficiency = abs(F_at_30kV) / P_dissipated if P_dissipated > 0 else 0.0
    
    print(f"\n  ── Operating Point: {V_ref/1e3:.0f} kV @ {F_ref/1e6:.0f} MHz ──")
    print(f"    Capacitance:     {C_gap*1e12:.3f} pF")
    print(f"    P_reactive:      {P_reactive:.2f} W")
    print(f"    P_dissipated:    {P_dissipated:.4f} W  (Q = {Q_estimated} — engineering estimate)")
    print(f"    Novel thrust:    {F_at_30kV*1e9:.4f} nN")
    print(f"    Efficiency:      {efficiency*1e9:.4f} nN/W")
    
    print(f"\n  KEY RESULT:")
    print(f"    Classical force (balanced by electrodes): {results[idx_30]['F_cls']*1e6:.2f} µN")
    print(f"    Novel AVE force (Axiom 4 differential):   {F_at_30kV*1e9:.4f} nN")
    print(f"    ► The classical force is internal — balanced by Newton's 3rd Law.")
    print(f"    ► The Axiom 4 differential is NOT balanced — it acts on the VACUUM.")
    print(f"    ► This is the measurable anomaly.")
    
    # ══════════════════════════════════════════════════════════════════════════
    # PLOTTING
    # ══════════════════════════════════════════════════════════════════════════
    
    C_BG = "#0a0a1a"
