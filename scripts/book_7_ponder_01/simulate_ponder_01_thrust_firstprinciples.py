"""
PONDER-01: First-Principles Thrust from Axiom 4 Saturation
============================================================

Derives the exact macroscopic ponderomotive thrust generated by an
asymmetric capacitor operating in the Axiom 4 non-linear vacuum.

DERIVATION CHAIN (zero magic numbers):
──────────────────────────────────────
1. GEOMETRY: Tip-plate capacitor with aspect ratio R = d/r_tip.
   E-field is hyperboloid-on-plane: E(z) = V / (z × ln(2d/r_tip)).
   The field is asymmetric: E_tip >> E_plate.

2. AXIOM 4 SATURATION: The local permittivity depends on field amplitude:
   ε_eff(z) = ε₀ × S(V_local / V_yield)
            = ε₀ × √(1 − (E(z)·Δz / V_yield)²)
   Near the sharp tip, E is large → ε_eff drops → the lattice stiffens.
   Near the flat plate, E is small → ε_eff ≈ ε₀ (linear).

3. ENERGY DENSITY: u(z) = ½ ε_eff(z) |E(z)|²
   Because ε_eff is spatially modulated by the field, u(z) is NOT symmetric.
   The stiffened (tip) side stores LESS energy per unit E² than the soft
   (plate) side. This breaks the spatial energy symmetry.

4. PONDEROMOTIVE FORCE: F = -du/dz  [N/m³]
   The gradient of the asymmetric energy density produces a NET force
   even in a sealed volume. This force pushes the dielectric from
   high-ε (soft/plate side) toward low-ε (stiff/tip side).

5. THRUST: F_total = ∫ F dV over the electrode gap volume.
   Input power: P_in = ½ ω C V² × (1/Q) (dissipated power at resonance).
   Efficiency: η = F_total / P_in  [N/W].

KEY PREDICTION:
   ► In linear mode (ε = ε₀ everywhere): F = 0 exactly (standard EM).
   ► In Axiom 4 mode: F ≠ 0. This is the novel AVE prediction.

All constants from ave.core.constants. Zero empirical fits.

Usage:
    PYTHONPATH=src python scripts/book_7_ponder_01/simulate_ponder_01_thrust_firstprinciples.py
"""

